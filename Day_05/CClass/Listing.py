list = [1,2,3,4,5,6,7,8]
list.append(10) # adds new element into list
print(len(list)) #prints length of the list
list.insert(0,20) # inserts a particular object at required index
print(list)
list.pop(3) #enables us to remove the element at 3rd index
print(list)
list.reverse() #sorts list in reversed
print(list)
list.sort() #sorts the list in ascending order
print(list)
min(list) #prints minimum value of the list
max(list) #prints maximum value of list
print(list.index(20)) #prints the index value of an object
print(list.count(2)) #prints the number of objects repeating in the list
